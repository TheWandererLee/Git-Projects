					<div style="font-size: 20px; font-family: Impact, Verdana;" align="center">Star News</div>
					<br />&nbsp;&nbsp;&nbsp;&nbsp;Four generations of the Jones family have been building in the Southport area and on Oak Island and Bald Head Island since the 1890s.
					<br />&nbsp;&nbsp;&nbsp;&nbsp;Great-grandfather Lewis Jones started with a few struc-tures, including a church. Then his son, Robert L. Jones, offi-cially started the company that became R.L. Jones and Sons Inc. in the 1930s.
					<br />&nbsp;&nbsp;&nbsp;&nbsp;After a stint in the Navy dur-ing World War II as a construction inspector Robert L. Jones returned to Southport and kept on building. "Our family has built about 350 houses on Oak Island over the time," his son Bobby said.
					<br />&nbsp;&nbsp;&nbsp;&nbsp;Bobby Jones, 75, followed his father's lead and entered the armed services, spending time in the Army, but he did not go right into the construction business. Bobby said he worked as a civil engineer for close to 30 years.
					<br />&nbsp;&nbsp;&nbsp;&nbsp;"I retired from Sunny Point in 1985 and went to work the next day for the construction com-pany," Bobby Jones said.
					<br />&nbsp;&nbsp;&nbsp;&nbsp;In the meantime, Bobby's son, Scott, 45, had gone to work for his grandfather. "I can remember working for him back when I was 10 or 12. After about the age of 121 spent every summer with him," Scott said, "I played a lot. It wasn't like I had my nose to the grindstone the entire time."
					<br />&nbsp;&nbsp;&nbsp;&nbsp;Scott's brother, Jon, 50, also came on board the family busi-ness. Scott is the president and Jon is the vice president of the company.
					<br />&nbsp;&nbsp;&nbsp;&nbsp;Scott said R.L. Jones and Sons Inc. is an independent company. "We just work for ourselves. We don't do any advertising, except by word of mouth," he said.
					<br />&nbsp;&nbsp;&nbsp;&nbsp;The main part of the business is building custom homes. "We build a couple of homes a year. We're not into volume," Scott said.
					<br />&nbsp;&nbsp;&nbsp;&nbsp;He said he liked building custom homes because he was able to work directly with the home-builders and give them "exactly what they want."
					<br />&nbsp;&nbsp;&nbsp;&nbsp;"You also stay on the job from beginning to end," he added.
					<br />&nbsp;&nbsp;&nbsp;&nbsp;Hurricane codes have changed the building business in recent years, and Scott Jones said it was a challenge to keep up with them.
					<br />&nbsp;&nbsp;&nbsp;&nbsp;"Now the main thing for the hurricane code is trying to keep houses tied to the ground," Scott said. "There are hundred-year-old houses that are still standing that weren't tied down, but now we spend a lot of time doing that."
					<br />&nbsp;&nbsp;&nbsp;&nbsp;Scott said after Hurricane Hazel hit North Carolina in the 1950s there were only three houses left on Long Beach. "One we owned and the other one we built," he said. The other house standing was the Capel House, located on top of Big Hill above the storm surge.
					<br />&nbsp;&nbsp;&nbsp;&nbsp;"I've enjoyed working with my family. My grandfather taught me the business, and with my dad we just try to keep up with the standards of the industry," Scott said. "We're lucky we all get along."
